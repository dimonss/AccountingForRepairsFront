import { useState, useEffect } from 'react'
import { useUpdateRepairMutation, useUploadRepairPhotosMutation } from '../store/api/repairsApi'
import type { Repair, RepairPhoto } from '../store/api/repairsApi'
import Modal from './Modal'
import { BarcodeScanner } from './BarcodeScanner'
import { PhotoUpload } from './PhotoUpload'

interface RepairEditFormProps {
  repair: Repair
  isOpen: boolean
  onSuccess: () => void
  onCancel: () => void
}

const RepairEditForm = ({ repair, isOpen, onSuccess, onCancel }: RepairEditFormProps) => {
  const [updateRepair, { isLoading }] = useUpdateRepairMutation()
  const [uploadPhotos] = useUploadRepairPhotosMutation()
  const [showBarcodeScanner, setShowBarcodeScanner] = useState(false)
  
  const [formData, setFormData] = useState<Partial<Repair>>({
    device_type: '',
    brand: '',
    model: '',
    serial_number: '',
    client_name: '',
    client_phone: '',
    client_email: '',
    issue_description: '',
    repair_status: 'pending',
    estimated_cost: 0,
    actual_cost: 0,
    notes: '',
    photos: []
  })

  // Pre-populate form with existing repair data
  useEffect(() => {
    if (repair) {
      setFormData({
        device_type: repair.device_type || '',
        brand: repair.brand || '',
        model: repair.model || '',
        serial_number: repair.serial_number || '',
        client_name: repair.client_name || '',
        client_phone: repair.client_phone || '',
        client_email: repair.client_email || '',
        issue_description: repair.issue_description || '',
        repair_status: repair.repair_status || 'pending',
        estimated_cost: repair.estimated_cost || 0,
        actual_cost: repair.actual_cost || 0,
        notes: repair.notes || '',
        photos: repair.photos || []
      })
    }
  }, [repair])

  const handleChange = (e: React.ChangeEvent<HTMLInputElement | HTMLTextAreaElement | HTMLSelectElement>) => {
    const { name, value } = e.target
    setFormData(prev => ({
      ...prev,
      [name]: name === 'estimated_cost' || name === 'actual_cost' 
        ? parseFloat(value) || 0 
        : value
    }))
  }

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    
    if (!repair.id) return
    
    try {
      // Update repair without photos first
      const { photos, ...repairData } = formData
      await updateRepair({ 
        id: repair.id, 
        repair: repairData 
      }).unwrap()
      
      // Handle photos separately - upload new ones
      if (photos && photos.length > 0) {
        const newPhotos = photos.filter(photo => !photo.id || photo.url.startsWith('data:'))
        if (newPhotos.length > 0) {
          try {
            await uploadPhotos({ repairId: repair.id, photos: newPhotos }).unwrap()
          } catch (photoError) {
            console.error('Failed to upload new photos:', photoError)
          }
        }
      }
      
      onSuccess()
    } catch (error) {
      console.error('Failed to update repair:', error)
    }
  }

  const handleBarcodeScanned = (scannedCode: string) => {
    setFormData(prev => ({
      ...prev,
      serial_number: scannedCode
    }))
    setShowBarcodeScanner(false)
  }

  const handlePhotosChange = (photos: RepairPhoto[]) => {
    setFormData(prev => ({
      ...prev,
      photos
    }))
  }

  const handleOpenScanner = () => {
    setShowBarcodeScanner(true)
  }

  const handleCloseScanner = () => {
    setShowBarcodeScanner(false)
  }

  return (
    <Modal
      isOpen={isOpen}
      onClose={onCancel}
      title={`–†–µ–¥–∞–∫—Ç–∏—Ä–æ–≤–∞—Ç—å –†–µ–º–æ–Ω—Ç #${repair.id}`}
    >
      <div className="repair-edit-modal-content">
        {/* Scrollable form content */}
        <div className="repair-edit-scrollable">
          <form onSubmit={handleSubmit}>
            <div className="form-grid">
              {/* –û—Å–Ω–æ–≤–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ–± —É—Å—Ç—Ä–æ–π—Å—Ç–≤–µ - –ø–µ—Ä–≤–∞—è —Å—Ç—Ä–æ–∫–∞ */}
              <div className="form-group">
                <label htmlFor="device_type">–¢–∏–ø –£—Å—Ç—Ä–æ–π—Å—Ç–≤–∞ *</label>
                <select
                  id="device_type"
                  name="device_type"
                  value={formData.device_type}
                  onChange={handleChange}
                  required
                >
                  <option value="">–í—ã–±–µ—Ä–∏—Ç–µ —Ç–∏–ø —É—Å—Ç—Ä–æ–π—Å—Ç–≤–∞</option>
                  <option value="smartphone">–°–º–∞—Ä—Ç—Ñ–æ–Ω</option>
                  <option value="tablet">–ü–ª–∞–Ω—à–µ—Ç</option>
                  <option value="laptop">–ù–æ—É—Ç–±—É–∫</option>
                  <option value="desktop">–ù–∞—Å—Ç–æ–ª—å–Ω—ã–π –ü–ö</option>
                  <option value="printer">–ü—Ä–∏–Ω—Ç–µ—Ä</option>
                  <option value="monitor">–ú–æ–Ω–∏—Ç–æ—Ä</option>
                  <option value="other">–î—Ä—É–≥–æ–µ</option>
                </select>
              </div>

              <div className="form-group">
                <label htmlFor="brand">–ë—Ä–µ–Ω–¥ *</label>
                <input
                  type="text"
                  id="brand"
                  name="brand"
                  value={formData.brand}
                  onChange={handleChange}
                  required
                />
              </div>

              <div className="form-group">
                <label htmlFor="model">–ú–æ–¥–µ–ª—å *</label>
                <input
                  type="text"
                  id="model"
                  name="model"
                  value={formData.model}
                  onChange={handleChange}
                  required
                />
              </div>

              {/* –í—Ç–æ—Ä–∞—è —Å—Ç—Ä–æ–∫–∞ - –¥–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –∏ —Å—Ç–∞—Ç—É—Å */}
              <div className="form-group">
                <label htmlFor="serial_number">–°–µ—Ä–∏–π–Ω—ã–π –ù–æ–º–µ—Ä</label>
                <div className="input-with-scanner">
                  <input
                    type="text"
                    id="serial_number"
                    name="serial_number"
                    value={formData.serial_number}
                    onChange={handleChange}
                    placeholder="–í–≤–µ–¥–∏—Ç–µ –∏–ª–∏ –æ—Ç—Å–∫–∞–Ω–∏—Ä—É–π—Ç–µ —Å–µ—Ä–∏–π–Ω—ã–π –Ω–æ–º–µ—Ä"
                  />
                  <button
                    type="button"
                    className="scanner-btn"
                    onClick={handleOpenScanner}
                    title="–°–∫–∞–Ω–∏—Ä–æ–≤–∞—Ç—å —à—Ç—Ä–∏—Ö–∫–æ–¥"
                  >
                    üì∑
                  </button>
                </div>
              </div>

              <div className="form-group">
                <label htmlFor="repair_status">–°—Ç–∞—Ç—É—Å –†–µ–º–æ–Ω—Ç–∞</label>
                <select
                  id="repair_status"
                  name="repair_status"
                  value={formData.repair_status}
                  onChange={handleChange}
                >
                  <option value="pending">–û–∂–∏–¥–∞–µ—Ç</option>
                  <option value="in_progress">–í —Ä–∞–±–æ—Ç–µ</option>
                  <option value="waiting_parts">–û–∂–∏–¥–∞–Ω–∏–µ –∑–∞–ø—á–∞—Å—Ç–µ–π</option>
                  <option value="completed">–ó–∞–≤–µ—Ä—à—ë–Ω</option>
                  <option value="cancelled">–û—Ç–º–µ–Ω—ë–Ω</option>
                </select>
              </div>

              <div className="form-group">
                {/* –ü—É—Å—Ç–æ–µ –º–µ—Å—Ç–æ –¥–ª—è —Å–∏–º–º–µ—Ç—Ä–∏–∏ */}
              </div>

              {/* –¢—Ä–µ—Ç—å—è —Å—Ç—Ä–æ–∫–∞ - –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—è –æ –∫–ª–∏–µ–Ω—Ç–µ */}
              <div className="form-group">
                <label htmlFor="client_name">–ò–º—è –ö–ª–∏–µ–Ω—Ç–∞ *</label>
                <input
                  type="text"
                  id="client_name"
                  name="client_name"
                  value={formData.client_name}
                  onChange={handleChange}
                  required
                />
              </div>

              <div className="form-group">
                <label htmlFor="client_phone">–¢–µ–ª–µ—Ñ–æ–Ω –ö–ª–∏–µ–Ω—Ç–∞ *</label>
                <input
                  type="tel"
                  id="client_phone"
                  name="client_phone"
                  value={formData.client_phone}
                  onChange={handleChange}
                  required
                />
              </div>

              <div className="form-group">
                <label htmlFor="client_email">Email –ö–ª–∏–µ–Ω—Ç–∞</label>
                <input
                  type="email"
                  id="client_email"
                  name="client_email"
                  value={formData.client_email}
                  onChange={handleChange}
                />
              </div>

              {/* –ß–µ—Ç–≤–µ—Ä—Ç–∞—è —Å—Ç—Ä–æ–∫–∞ - —Å—Ç–æ–∏–º–æ—Å—Ç—å */}
              <div className="form-group">
                <label htmlFor="estimated_cost">–ü—Ä–µ–¥–≤–∞—Ä–∏—Ç–µ–ª—å–Ω–∞—è –°—Ç–æ–∏–º–æ—Å—Ç—å (‚ÇΩ)</label>
                <input
                  type="number"
                  id="estimated_cost"
                  name="estimated_cost"
                  value={formData.estimated_cost}
                  onChange={handleChange}
                  min="0"
                  step="0.01"
                />
              </div>

              <div className="form-group">
                <label htmlFor="actual_cost">–§–∞–∫—Ç–∏—á–µ—Å–∫–∞—è –°—Ç–æ–∏–º–æ—Å—Ç—å (‚ÇΩ)</label>
                <input
                  type="number"
                  id="actual_cost"
                  name="actual_cost"
                  value={formData.actual_cost}
                  onChange={handleChange}
                  min="0"
                  step="0.01"
                />
              </div>

              <div className="form-group">
                {/* –ü—É—Å—Ç–æ–µ –º–µ—Å—Ç–æ */}
              </div>
            </div>

            <div className="form-group full-width">
              <label htmlFor="issue_description">–û–ø–∏—Å–∞–Ω–∏–µ –ü—Ä–æ–±–ª–µ–º—ã *</label>
              <textarea
                id="issue_description"
                name="issue_description"
                value={formData.issue_description}
                onChange={handleChange}
                required
                rows={3}
              />
            </div>

            <div className="form-group full-width">
              <label htmlFor="notes">–î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω—ã–µ –ó–∞–º–µ—Ç–∫–∏</label>
              <textarea
                id="notes"
                name="notes"
                value={formData.notes}
                onChange={handleChange}
                rows={3}
              />
            </div>

            <div className="form-group full-width">
              <PhotoUpload
                photos={formData.photos || []}
                onPhotosChange={handlePhotosChange}
                maxPhotos={5}
                disabled={isLoading}
                repairId={repair.id}
              />
            </div>
          </form>
        </div>

        {/* Fixed footer with action buttons */}
        <div className="repair-edit-footer">
          <div className="form-actions">
            <button 
              type="button" 
              onClick={onCancel}
              className="cancel-btn"
              disabled={isLoading}
            >
              –û—Ç–º–µ–Ω–∞
            </button>
            <button 
              type="submit" 
              disabled={isLoading} 
              className="submit-btn"
              onClick={handleSubmit}
            >
              {isLoading ? '–°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ...' : '–°–æ—Ö—Ä–∞–Ω–∏—Ç—å –ò–∑–º–µ–Ω–µ–Ω–∏—è'}
            </button>
          </div>
        </div>
      </div>

      {/* Barcode Scanner Modal */}
      <BarcodeScanner
        isOpen={showBarcodeScanner}
        onClose={handleCloseScanner}
        onScan={handleBarcodeScanned}
      />
    </Modal>
  )
}

export default RepairEditForm 